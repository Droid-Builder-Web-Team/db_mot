                            <p class="mb-3">When BC parts are purchased via the forum it is audited and allows us to see when people signed up.  Then once the parts have been produced the payment can be made and the parts shipped.  This is important as the seller has the initial outlay of the costs, then the parts ship when available rather than taking money up front and then shipping months later. </p>
